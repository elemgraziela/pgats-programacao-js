/**
 * + - adição
 * - - subtração
 * * -  multiplicação
 * / - divisão
 * ** - exponencial
 */

console.log(1 + 1) //2
console.log(10 - 7) //3
console.log(2 * 2) //4
console.log(10 / 2) //5
console.log(11 % 2) //resto 1
console.log(4 ** 4) //exponencial
console.log(1 + 1 * 4) //precedência - multiplicação, adição
console.log((2 + 3) * 4) //precedencia - dentro do parenteses, depois o que esta fora
console.log(+"5" * 2)